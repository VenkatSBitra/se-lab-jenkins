#!/usr/bin/python3

def add_nums(num1, num2):
    return num1 + num2

def sub_nums(num1, num2):
    return num1 - num2

def mul_nums(num1, num2):
    return num1 * num2

def div_nums(num1, num2):
    return num1 / num2

def avg_nums(num_list):
    return div_nums(sum(num_list), len(num_list))